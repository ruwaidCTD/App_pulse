  <script>
    function openTab(event, tabName) {
  var i, tabContent, tabs;

  // Hide all tab-highlightcontent
  tabContent = document.getElementsByClassName("tab-pane");
  for (i = 0; i < tabContent.length; i++) {
    tabContent[i].style.display = "none";
  }

  // Remove the active class from all tab-highlightbuttons
  tabs = document.getElementsByClassName("tab-highlight");
  for (i = 0; i < tabs.length; i++) {
    tabs[i].classList.remove("active-tab");
  }

  // Show the clicked tab content and add an active class to the button
  document.getElementById(tabName).style.display = "block";
  event.currentTarget.classList.add("active-tab");
}

// Default to show the first tab
document.getElementsByClassName("tab-highlight")[0].click();

