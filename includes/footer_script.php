$(document).ready(function () {
  const modal = $('#modal'); // Reference to modal container
  const modalBox = $('.modal-box'); // Reference to the modal content box

  // Open Modal Animation
  $('.open-modal').on('click', function () {
    modal.css('display', 'flex'); // Make modal visible
    const xPos = window.innerWidth > 768 ? 310 : 0; // Adjust position for smaller screens

    gsap.fromTo(
      modalBox,
      { x: xPos, opacity: 0 }, // Start position and opacity
      { x: 0, opacity: 1, duration: 0.5, ease: 'power2.out' } // End position and fade in
    );
  });

  // Close Modal Animation
  $('.close-modal').on('click', function () {
    gsap.to(modalBox, {
      x: 310, // Move the modal box offscreen
      opacity: 0, // Fade out
      duration: 0.4, // Duration of the closing animation
      ease: 'power2.in', // Easing for a smoother transition
      onComplete: function () {
        modal.css('display', 'none'); // Hide modal after animation completes
      }
    });
  });

  // Close Modal when clicking outside the modal box
  modal.on('click', function (e) {
    if ($(e.target).is(modal)) { // Check if the clicked target is the modal background
      gsap.to(modalBox, {
        x: 310, // Move offscreen
        opacity: 0, // Fade out
        duration: 0.4, // Duration of the closing animation
        ease: 'power2.in', // Easing for smoother transition
        onComplete: function () {
          modal.css('display', 'none'); // Hide modal after animation completes
        }
      });
    }
  });
});

  // Scroll To Top
  function scrollToTop() {
    window.scrollTo({
      top: 0,
      behavior: 'smooth'
    });
  }

